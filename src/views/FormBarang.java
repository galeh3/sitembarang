    private void load_satuan(){
        List<Satuan> list = satuan.getList();
        String[] nama = new String[list.size()];
        for(Integer i = 0; i < list.size(); i++){
            nama[i] = list.get(i).nama;
        }
        jComboBoxSatuan.setModel(new javax.swing.DefaultComboBoxModel<>(nama));
    }

    private void load_table() {
        // membuat tampilan model tabel
        if(barang.bacaData()){
            jTableBarang.setModel(new javax.swing.table.DefaultTableModel(
                barang.getList(),
                new String [] {
                    "Kode Barang", "Nama Barang", "Satuan", "Jumlah", "Harga"
                }
            ) {
                Class[] types = new Class [] {
                    java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class
                };

                public Class getColumnClass(int columnIndex) {
                    return types [columnIndex];
                }
            });
        }
    }

