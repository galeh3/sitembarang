    private void jTableBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableBarangMouseClicked
        // TODO add your handling code here:
        int baris = jTableBarang.rowAtPoint(evt.getPoint());
        String kode = jTableBarang.getValueAt(baris, 0).toString();
        kdbarang.setText(kode);
        kdbarang.enable(false);
        String nma = jTableBarang.getValueAt(baris, 1).toString();
        nama.setText(nma);
        String tuan = jTableBarang.getValueAt(baris, 2).toString();
        jComboBoxSatuan.setSelectedIndex(satuan.findIndex(tuan));
        String jmlh = jTableBarang.getValueAt(baris, 3).toString();
        jumlah.setText(jmlh);
        String hrg = jTableBarang.getValueAt(baris, 4).toString();
        harga.setText(hrg);
        btnInsert.setEnabled(false);
        btnDelete.setEnabled(true);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_jTableBarangMouseClicked

